byte** fillmask(byte mask[H][W],siz h, siz w)
{
    int i,j;
    byte **array = malloc(sizeof(*array)*h);
    for (i=0;i<h;i++)
    {
        array[i] = (byte*)malloc(sizeof(byte)*w);
        for (j=0;j<w;j++)
        {
            array[i][j] = mask[i][j];
        }
    }
    return(array);    
}

byte* ravel(byte array[H][W],siz h, siz w)
{
    int i,j,k=0,n = h*w;
    byte *output = malloc(sizeof(byte)*n);
    for (i=0;i<w;i++)
    {
        for (j=0;j<h;j++)
        {
            output[k] = array[j][i];
            k++;
        }
    }
    return(output);
}

byte** reshape(byte *in, siz h, siz w)
{
    int i,j,k=0;
    byte **out = malloc(sizeof(*out)*h);
    
    for (i=0;i<h;i++)
    {                
        out[i] = (byte*)malloc(sizeof(byte)*w);        
        for (j=0;j<w;j++)
        {            
            out[i][j] =  in[h*j+i];
            k = k + 1;
        }        
    }
    return(out);
}

void arrayprint(byte **array, siz h, siz w)
{
    int i,j;
    for (i=0;i<h;i++)
    {
        for (j=0;j<w;j++)
        {
            printf("%d ",array[i][j]);
        }
        printf("\n");
    }
}

void freemat(byte **fmask,siz h)
{
    int i;
    for (i=0;i<h;i++)
    {
        free(fmask[i]);        
    }
    free(fmask);    
}

